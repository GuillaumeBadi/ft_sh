#include "ft_minishell1.h"

char				*ft_subc(char *s, char c)
{
	int				i;
	char			*str;

	i = 0;
	str = NULL;
	while (s[i] && s[i] != c)
		i++;
	if (s[i] == c)
		str = ft_strndup(s, i);
	return (str);
}

